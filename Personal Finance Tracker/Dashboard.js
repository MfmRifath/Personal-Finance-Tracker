function navigateTo(page) {
    if(page == "accountBalances"){
        window.location.replace("Accountbalance.html");
    }
    else if(page =="recentTransactions"){
        window.location.replace("RecentTransactions.html");
    }
    else if(page =="budgetOverview"){
        window.location.replace("BudgetOverview.html");
    }
    else if(page=="trackExpenses"){
        window.location.replace("TrackExpanses.html")
    }

}
